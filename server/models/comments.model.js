import mongoose from "mongoose";

const commentSchema = new mongoose.Schema({
    userId:{
        type:mongoose.Schema.ObjectId,
        ref:"User",
        required:true
    },
    postId:{
        type:mongoose.Schema.ObjectId,
        ref:"Post",
        required:true
    },
    body:{
        type:String,
        required:true
    }
});

const Comment = mongoose.model("Comment",commentSchema);

export default Comment;